//module.exports Helpers
exports.getDate = (period)  => {
    var now = moment();
    let prevDate;
    switch (period) {
        case "3m":
            prevDate = now.add(-3, "M").format('YYYY-MM-DD')
        break;
        case "6m":
            prevDate = now.add(-5, "M").format('YYYY-MM-DD')
        break;
        case "1y":
            prevDate = now.add(-1, "y").format('YYYY-MM-DD')
        break;
        default:
            prevDate = now.add(-1, "M").format('YYYY-MM-DD')
    }
    return(prevDate)
 
}


